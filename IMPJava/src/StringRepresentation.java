
public class StringRepresentation {

	public static void main(String[] args) {

		// Representing String by using String Class
		
		String str1=new String ("Ganesh");
		String str2="Ganesh";
		
		// Representing String by using StringBuffer Class
		
		StringBuffer str3=new StringBuffer("Ganesh");
		
		// Representing String by using StringBuilder Class
		
		StringBuilder str4=new StringBuilder("Ganesh");
	}

}
